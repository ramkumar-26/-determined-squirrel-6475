package com.crimetime.model;

public class PoliceBean {
	
	private int police_id;
	private String name;
	private String assigned_policestation;
	private String password;
	private String access_status;
	public int getPolice_id() {
		return police_id;
	}
	public void setPolice_id(int police_id) {
		this.police_id = police_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAssigned_policestation() {
		return assigned_policestation;
	}
	public void setAssigned_policestation(String assigned_policestation) {
		this.assigned_policestation = assigned_policestation;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getAccess_status() {
		return access_status;
	}
	public void setAccess_status(String access_status) {
		this.access_status = access_status;
	}
	public PoliceBean(int police_id, String name, String assigned_policestation, String password,
			String access_status) {
		super();
		this.police_id = police_id;
		this.name = name;
		this.assigned_policestation = assigned_policestation;
		this.password = password;
		this.access_status = access_status;
	}
	@Override
	public String toString() {
		return "PoliceBean [police_id=" + police_id + ", name=" + name + ", assigned_policestation="
				+ assigned_policestation + ", password=" + password + ", access_status=" + access_status + "]";
	}
	
	
	public PoliceBean() {
		//empty
	}
	
}
